class SpriteData {
  const SpriteData({
    required this.minX,
    required this.minY,
    required this.maxX,
    required this.maxY,
    required this.srcX,
    required this.srcY,
    required this.srcWidth,
    required this.srcHeight,
  });

  final double minX;
  final double minY;
  final double maxX;
  final double maxY;
  final double srcX;
  final double srcY;
  final double srcWidth;
  final double srcHeight;
}
