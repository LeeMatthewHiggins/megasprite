class SpriteData {
  const SpriteData({
    required this.x,
    required this.y,
    required this.width,
    required this.height,
    required this.atlasX,
    required this.atlasY,
    required this.atlasWidth,
    required this.atlasHeight,
  });

  final double x;
  final double y;
  final double width;
  final double height;
  final double atlasX;
  final double atlasY;
  final double atlasWidth;
  final double atlasHeight;
}
