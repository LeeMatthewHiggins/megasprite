class SpriteData {
  const SpriteData({
    required this.minX,
    required this.minY,
    required this.maxX,
    required this.maxY,
    required this.atlasMinX,
    required this.atlasMinY,
    required this.atlasMaxX,
    required this.atlasMaxY,
  });

  final double minX;
  final double minY;
  final double maxX;
  final double maxY;
  final double atlasMinX;
  final double atlasMinY;
  final double atlasMaxX;
  final double atlasMaxY;
}
